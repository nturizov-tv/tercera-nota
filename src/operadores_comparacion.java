public class operadores_comparacion {
    public static void main(String[] args) {
        
        int numero1 = 20;
        int numero2 = 30;

        System.out.println("¿El numero1 es igual al numero 2? "+(numero1==numero2));
        System.out.println("¿El numero1 es diferente al numero 2? "+(numero1!=numero2));
        System.out.println("¿El numero1 es menor que el numero 2? "+(numero1<numero2));
        System.out.println("¿El numero1 es mayor que el numero 2? "+(numero1>numero2));
        System.out.println("¿El numero1 es menor o igual que el numero 2? "+(numero1<=numero2));
        System.out.println("¿El numero1 es mayor o igual que el numero 2? "+(numero1>=numero2));
    }
}